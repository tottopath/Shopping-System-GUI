    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            String url = "jdbc:mysql://localhost:3306/first_database";
            String uname = "root";
            String pass = "babatotto@20";
            Connection con = DriverManager.getConnection(url, uname, pass);

            Statement stm = con.createStatement();

            String username = edtUsername.getText();
            String password = edtPassword.getText();

            String sql = "SELECT * FROM login WHERE user_name='" + username + "' && passkey='" + password + "'";
            ResultSet rs = stm.executeQuery(sql);

            if (rs.next()){

                dispose();
                MainPage Mpage = new MainPage();
                Mpage.show();
            }
            else{
                JOptionPane.showMessageDialog(this, "Username and/or Password are wrong");
                edtUsername.setText("");
                edtPassword.setText("");
            }

            con.close();

        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

